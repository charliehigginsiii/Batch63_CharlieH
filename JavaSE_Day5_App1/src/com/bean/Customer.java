package com.bean;

public class Customer {
	private int custId;
	private String custName;
	private String custLoc;
	private long custPhno;
	public Customer() {}
	public int getCustId() {
		return custId;
	}
	public void setCustId(int custId) {
		this.custId = custId;
	}
	public String getCustName() {
		return custName;
	}
	public void setCustName(String custName) {
		this.custName = custName;
	}
	public String getCustLoc() {
		return custLoc;
	}
	public void setCustLoc(String custLoc) {
		this.custLoc = custLoc;
	}
	public long getCustPhno() {
		return custPhno;
	}
	public void setCustPhno(long custPhno) {
		this.custPhno = custPhno;
	}
	

}
