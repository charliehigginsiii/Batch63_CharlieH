package Operators;

public class Operators1 {

	public static void main(String[] args) {
		System.out.println("Operators 1");
		int a=100;
		int b=7;
		System.out.println("a+b ---->"+(a+b));
		System.out.println("a+b ---->"+(a-b));
		System.out.println("a+b ---->"+(a*b));
		System.out.println("a+b ---->"+(a/b));
		System.out.println("a+b ---->"+(a%b));
	}

}
