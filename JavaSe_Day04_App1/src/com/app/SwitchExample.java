package com.app;

public class SwitchExample {
	public static void main(String[]args) {
		int a=0;
		switch(a) {
		case 0:
			System.out.println("0zero");
			break;
		case 1:
			System.out.println("1one");
			break;
		case 2:
			System.out.println("2two");
			break;
		case 3:
			System.out.println("3three");
			break;
		default:
			System.out.println("default");
		}
	}
}
